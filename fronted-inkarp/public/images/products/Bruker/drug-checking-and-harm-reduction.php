        document.addEventListener("DOMContentLoaded", function() {
            // Function to extract query string value from URL
            function getQueryStringValue(key) {
                const urlParams = new URLSearchParams(window.location.search);
                return urlParams.get(key);
            }

            // Function to set query string value to input text box
            function setQueryStringValueToInput() {
                const queryStringValue = getQueryStringValue('product');
                const inputBox = document.getElementById('product');
                if (queryStringValue) {
                    inputBox.value = queryStringValue;
                }
            }

            // Extract current URL and filename
            var currentUrl = window.location.href;
            var url = new URL(currentUrl);
            var pathname = url.pathname;
            var filename = pathname.substring(pathname.lastIndexOf('/') + 1);
            var fileWithoutExtension = filename.split('.').slice(0, -1).join('.');

            // Check if the URL does not already contain the product query parameter
            if (!url.searchParams.has('product')) {
                // Create the new URL with the query parameter
                url.searchParams.set('product', fileWithoutExtension);

                // Replace the current URL with the new URL
                window.location.replace(url.toString());
            }

            // Event listener for button click to trigger setting query string value to input text box
            document.querySelector('.theme-btn').addEventListener('click', setQueryStringValueToInput);
        });

